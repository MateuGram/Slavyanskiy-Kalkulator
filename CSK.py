# Пример: калькулятор с интерфейсом на церковнославянском (упрощённая стилизация)

цсл_словарь = {
    "выбор": "Избери дѣло:",
    "сложить": "сложити",
    "вычесть": "вычѧсти",
    "умножить": "умножити",
    "разделить": "раздѣлити",
    "результат": "исходъ",
    "ошибка": "грѣхъ въ вводѣ"
}

def главная():
    print(f"{цсл_словарь['выбор']}")
    print(f"1. {цсл_словарь['сложить']}")
    print(f"2. {цсл_словарь['вычесть']}")
    
    выбор = input("Въведи число (1 или 2): ")
    а = float(input("Число а: "))
    в = float(input("Число в: "))
    
    if выбор == "1":
        исход = а + в
    elif выбор == "2":
        исход = а - в
    else:
        print(цсл_словарь['ошибка'])
        return
    
    print(f"{цсл_словарь['результат']}: {исход}")

if __name__ == "__main__":
    главная()
